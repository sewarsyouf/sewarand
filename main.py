class Extractor:
    def __init__(self):
        self.skill_patterns = {
            skill: [
                re.compile(rf"(?<![a-zA-Z0-9]){re.escape(alias)}(?![a-zA-Z0-9])", re.IGNORECASE)
                for alias in aliases
            ]
            for skill, aliases in Config.SKILL_ALIASES.items()
        }

    def extract_skills(self, text):
        found_skill_ids = set()
        for skill, patterns in self.skill_patterns.items():
            if any(pattern.search(text) for pattern in patterns):
                skill_id = Config.SKILL_ALIAS_KEY_TO_ID.get(skill.lower())
                if skill_id is not None:
                    found_skill_ids.add(skill_id)
        return sorted(list(found_skill_ids))

    def extract_years(self, text):
        lower_text = text.lower()
        years_found = []
        patterns = [
            r"(\d{1,2})\+?\s*(?:years|yrs)\s+(?:of\s+)?experience",
            r"experience\s*(?:of|in)?\s*(\d{1,2})\+?\s*(?:years|yrs)?"
        ]
        for pattern in patterns:
            for x in re.findall(pattern, lower_text):
                value = int(x)
                if 0 <= value <= 40:
                    years_found.append(value)
        return max(years_found) if years_found else 0

def extract_education_info(text):
    education_list = []
    seen_universities = set()

    degree_levels = [
        {"level": "PhD", "patterns": [r"\b(ph\.?d\.?|doctorate|doctor of philosophy)\b"]},
        {"level": "Master", "patterns": [r"\b(master(?:'s)?(?:\s+degree)?)\b"]},
        {"level": "Bachelor", "patterns": [r"\b(bachelor(?:'s)?(?:\s+degree)?)\b"]}
    ]
    university_patterns = [
        r"\b(University\s+of\s+[A-Z][A-Za-z&.\s]{2,40})\b",
        r"\b([A-Z][A-Za-z&.\s]{2,40}\sUniversity)\b",
        r"\b([A-Z][A-Za-z&.\s]{2,40}\sCollege)\b"
    ]
    gpa_patterns = [
        r"\b(?:gpa|cgpa|grade|g\.p\.a)\s*[:\-]?\s*([0-4]\.\d{1,3}|4\.0|[5-9]\d(?:\.\d{1,2})?%?)\b",
        r"\b([0-3]\.\d{1,2}|4\.0)\s*/\s*4\b"
    ]

    lines = text.split('\n')
    for i, line in enumerate(lines):
        if any(k in line.lower() for k in ["education", "university", "bachelor", "master", "bsc", "degree", "college"]):
            inst_name = None
            deg_name = None
            gpa_val = None
            from_d = None
            to_d = None

            context = line + " " + (lines[i+1] if i+1 < len(lines) else "")
            for pat in university_patterns:
                m = re.search(pat, context)
                if m: inst_name = m.group(1).strip(); break

            if inst_name:
                inst_name = clean_extracted_name(inst_name, ["From", "To", "Date", "GPA"])
                if inst_name.lower() in seen_universities or inst_name == "Not Found": continue

            for group in degree_levels:
                for pat in group["patterns"]:
                    m = re.search(pat, context, re.IGNORECASE)
                    if m: deg_name = group["level"]; break
                if deg_name: break

            for pat in gpa_patterns:
                m = re.search(pat, context, re.IGNORECASE)
                if m: gpa_val = m.group(1).strip(); break

            years = re.findall(r"\b(20\d{2}|19\d{2})\b", context)
            if len(years) >= 2:
                from_d = f"{years[0]}-01-01"
                to_d = f"{years[1]}-01-01"
            elif len(years) == 1:
                to_d = f"{years[0]}-01-01"

            if inst_name and inst_name != "Not Found":
                seen_universities.add(inst_name.lower())
                education_list.append({
                    "institutionName": inst_name,
                    "fromDate": from_d,
                    "toDate": to_d,
                    "degree": deg_name if deg_name else "Bachelor",
                    "gpa": gpa_val if gpa_val else None
                })
    return education_list

def extract_experience_info(text, extractor_instance):
    experience_list = []
    seen_companies = set()

    company_patterns = [
        r"\b([A-Z][A-Za-z0-9\s&.,]{2,30}\s(?:Ltd|Inc|Corp|Company|Solutions|Bank|Center|Institute|Group|Technologies|Software))\b",
        r"(?:Company|Employer)\s*[:\-]\s*([A-Za-z0-9\s&.,]{2,30})"
    ]

    blocks = text.split('\n')
    for i, line in enumerate(blocks):
        if any(keyword in line.lower() for keyword in ["engineer", "developer", "analyst", "manager", "intern"]):
            comp_name = "Not Found"
            from_d = None
            to_d = None
            job_title = line.strip()[:60]

            context_text = line + " " + (blocks[i+1] if i+1 < len(blocks) else "")
            for pat in company_patterns:
                m = re.search(pat, context_text, re.IGNORECASE)
                if m: comp_name = m.group(1).strip(); break

            if comp_name:
                comp_name = clean_extracted_name(comp_name, ["Experience", "Responsibilities", "Using", "Present"])
                if comp_name.lower() in seen_companies or comp_name == "Not Found": continue

            date_range = re.search(r"\b(20\d{2}|19\d{2})\s*[-–—]\s*(20\d{2}|19\d{2}|present|current|now)\b", context_text, re.IGNORECASE)
            if date_range:
                from_d = f"{date_range.group(1)}-01-01"
                to_d = "2026-01-01" if any(w in date_range.group(2).lower() for w in ["present", "current", "now"]) else f"{date_range.group(2)}-01-01"
            else:
                from_d = parse_date_safely(context_text)

            job_skills = extractor_instance.extract_skills(context_text)

            if comp_name != "Not Found":
                seen_companies.add(comp_name.lower())
                experience_list.append({
                    "companyName": comp_name,
                    "fromDate": from_d,
                    "toDate": to_d,
                    "jobTitle": job_title,
                    "description": context_text.strip()[:150] + "...",
                    "skills": job_skills
                })
    return experience_list[:5]

