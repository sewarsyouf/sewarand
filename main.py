def parse_date_safely(text_context):
    months_map = {
        "jan": "01", "feb": "02", "mar": "03", "apr": "04", "may": "05", "jun": "06",
        "jul": "07", "aug": "08", "sep": "09", "oct": "10", "nov": "11", "dec": "12"
    }
    numeric_range = re.findall(r"\b(0[1-9]|1[0-2])[-/](20\d{2}|19\d{2})\b", text_context)
    if numeric_range:
        return f"{numeric_range[0][1]}-{numeric_range[0][0]}-01"
    for month_name, month_num in months_map.items():
        if re.search(rf"\b{month_name}\b", text_context, re.IGNORECASE):
            year_match = re.search(r"\b(20\d{2}|19\d{2})\b", text_context)
            if year_match:
                return f"{year_match.group(1)}-{month_num}-01"
    year_only = re.search(r"\b(20\d{2}|19\d{2})\b", text_context)
    if year_only:
        return f"{year_only.group(1)}-01-01"
    return None

def clean_extracted_name(name, strip_words):
    if not name: return "Not Found"
    name = re.sub(r"[:\-,\.\(\)\t]", " ", name)
    for word in strip_words:
        name = re.sub(rf"\b{word}\b", "", name, flags=re.IGNORECASE)
    name = re.sub(r"\s+", " ", name).strip()
    return name if len(name) > 2 else "Not Found"

